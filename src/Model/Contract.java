package Model;

public class Contract {

    private String fillingDate;
    private String faculty;
    private String chair;
    private String city;
    private String street;
    private String house;
    private String flat;
    private String passport;
    private String passportDateOfIssue;

    public String getFillingDate() { return fillingDate; }
    public String getFaculty() { return faculty; }
    public String getChair() { return chair; }
    public String getCity() { return city; }
    public String getStreet() { return street; }
    public String getHouse() { return house; }
    public String getFlat() { return flat; }
    public String getPassport() { return passport; }
    public String getPassportDateOfIssue() { return passportDateOfIssue; }

    public void setFillingDate(String fillingDate) { this.fillingDate = fillingDate; }
    public void setFaculty(String faculty) { this.faculty = faculty; }
    public void setChair(String chair) { this.chair = chair; }
    public void setCity(String city) { this.city = city; }
    public void setStreet(String street) { this.street = street; }
    public void setHouse(String house) { this.house = house; }
    public void setFlat(String flat) { this.flat = flat; }
    public void setPassport(String passport) { this.passport = passport; }
    public void setPassportDateOfIssue(String passportDateOfIssue) { this.passportDateOfIssue = passportDateOfIssue; }

}
